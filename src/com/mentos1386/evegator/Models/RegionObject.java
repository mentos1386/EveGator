package com.mentos1386.evegator.Models;

public class RegionObject {

    private int id;
    private String name;

    public RegionObject(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }
}
